DROP TABLE MEMBER;

CREATE TABLE MEMBER(
	NAME VARCHAR2(100),
	ID VARCHAR2(100),
	PW VARCHAR2(100),
	TEL VARCHAR2(100),
	BIRTHDAY VARCHAR2(100),
	SEX VARCHAR2(100),
	SKINTONE VARCHAR2(100),
	SKINTYPE VARCHAR2(100),
	SKINPROBLEM VARCHAR2(100),
	CONSTRAINT NOSAMEID PRIMARY KEY ID
)


