DROP TABLE MYPOUCH;

CREATE TABLE MYPOUCH(
	ID VARCHAR2(100),
	PCH_NAME VARCHAR2(100),
	BRAND VARCHAR2(100),
	COLOR VARCHAR2(100),
	PERFO VARCHAR2(100),
	PTYPE VARCHAR2(100),
	PERR VARCHAR2(100),
	PART VARCHAR2(100),
	PARTTYPE VARCHAR2(100),
	OPENDATE VARCHAR2(100),
	VOLUME VARCHAR2(100),
	KEYWORD VARCHAR2(200),
	CONSTRAINT PCHFORONE_PK PRIMARY KEY (ID, PCH_NAME)
)


