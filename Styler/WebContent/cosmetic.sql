DROP TABLE COSMETIC;

CREATE TABLE COSMETIC(
	PCH_NAME VARCHAR2(100),
	PRICE VARCHAR2(100),
	BRAND VARCHAR2(100),
	COLOR VARCHAR2(100),
	PERFO VARCHAR2(100),
	PTYPE VARCHAR2(100),
	PERR VARCHAR2(100),
	PART VARCHAR2(100),
	PARTTYPE VARCHAR2(100),
	VOLUME VARCHAR2(100),
	KEYWORD VARCHAR2(200),
	CONSTRAINT SINGLECOSMETIC_PK PRIMARYKEY PCH_NAME
)


SELECT * FROM COSMETIC;